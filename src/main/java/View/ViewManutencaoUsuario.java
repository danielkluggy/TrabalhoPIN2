    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (modo.equals("Novo")) {
            ModelUsuario usuario = new ModelUsuario(tfNome.getText(), tfInstituicao.getText());
            this.view.salvarUsuario(usuario);
        } else if (modo.equals("Editar")) {
            int index = tbUsuario.getSelectedRow();
            ModelUsuario usuario = new ModelUsuario(this.view.getUsuarios().get(index).getCodigo(),tfNome.getText(), tfInstituicao.getText());
            this.view.editarUsuario(usuario);
        }

        modo = "Visualizar";
        manipularInterface();
        LoadTable();
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        int index = tbUsuario.getSelectedRow();
        if (index >= 0 && index < this.view.getUsuarios().size()) {
            int confirmacao = JOptionPane.YES_NO_OPTION;
            int excluir = JOptionPane.showConfirmDialog (null, "Deseja excluir esse usuário?","Warning",confirmacao);
            if(excluir == JOptionPane.YES_OPTION) {
                this.view.excluirUsuario(this.view.getUsuarios().get(index).getCodigo());
            }
        }
        LoadTable();
        modo = "Visualizar";
        manipularInterface();
    }//GEN-LAST:event_btExcluirActionPerformed

