    private void tbUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsuarioMouseClicked
        int index = tbUsuario.getSelectedRow();
        if (index >= 0 && index < this.view.getUsuarios().size()) {
            modo = "Selecionar";
            manipularInterface();
            tfNome.setText(this.view.getUsuarios().get(index).getNome());
            tfInstituicao.setText(this.view.getUsuarios().get(index).getInstituicao());
        }
    }//GEN-LAST:event_tbUsuarioMouseClicked

    private void manipularInterface () {
        switch (modo) {
            case "Visualizar":
                btAdicionar.setEnabled(true);
                btEditar.setEnabled(false);
                btExcluir.setEnabled(false);
                btSalvar.setEnabled(false);
                btCancelar.setEnabled(false);
                tfNome.setEnabled(false);
                tfInstituicao.setEnabled(false);
                tfNome.setText("");
                tfInstituicao.setText("");
                tbUsuario.clearSelection();
                break;
                
            case "Selecionar":
                btAdicionar.setEnabled(true);
                btEditar.setEnabled(true);
                btExcluir.setEnabled(true);
                btSalvar.setEnabled(false);
                btCancelar.setEnabled(false);
                tfNome.setEnabled(false);
                tfInstituicao.setEnabled(false);
                break;
            
            case "Novo":
                btAdicionar.setEnabled(false);
                btEditar.setEnabled(false);
                btExcluir.setEnabled(false);
                btSalvar.setEnabled(true);
                btCancelar.setEnabled(true);
                tfNome.setEnabled(true);
                tfInstituicao.setEnabled(true);
                tfNome.setText("");
                tfInstituicao.setText("");
                tbUsuario.clearSelection();
                break;
            
            case "Editar":
                btAdicionar.setEnabled(false);
                btEditar.setEnabled(false);
                btExcluir.setEnabled(false);
                btSalvar.setEnabled(true);
                btCancelar.setEnabled(true);
                tfNome.setEnabled(true);
                tfInstituicao.setEnabled(true);
                break;
                
            default: System.out.println("Modo inválido");
        }
    }
    
