    private void LoadTable() {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Código", "Nome", "Instituição"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.Integer.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        };
        for (int i = 0; i < this.view.getUsuarios().size(); i++) {
            Object linha[] = new Object[] { this.view.getUsuarios().get(i).getCodigo(),
                                                            this.view.getUsuarios().get(i).getNome(),
                                                            this.view.getUsuarios().get(i).getInstituicao()};
            modelo.addRow(linha);
        }
        tbUsuario.setModel(modelo);
    }
    
