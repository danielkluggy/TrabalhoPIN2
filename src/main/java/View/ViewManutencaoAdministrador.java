    private void LoadTable() {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Código", "Nome", "Cargo"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.Integer.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        };
        for (int i = 0; i < this.view.getAdministradores().size(); i++) {
            Object linha[] = new Object[] { this.view.getAdministradores().get(i).getCodigo(),
                                                            this.view.getAdministradores().get(i).getNome(),
                                                            this.view.getAdministradores().get(i).getCargo()};
            modelo.addRow(linha);
        }
        tbMotorista.setModel(modelo);
    }
    
