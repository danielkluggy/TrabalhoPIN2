    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (modo.equals("Novo")) {
            ModelAdministrador administrador = new ModelAdministrador(tfNome.getText(), tfCargo.getText());
            this.view.salvarAdministrador(administrador);
        } else if (modo.equals("Editar")) {
             int index = tbMotorista.getSelectedRow();
            ModelAdministrador administrador = new ModelAdministrador(this.view.getAdministradores().get(index).getCodigo(),tfNome.getText(), tfCargo.getText());
                this.view.editarAdministrador(administrador);
        }

        modo = "Visualizar";
        manipularInterface();
        LoadTable();
    }//GEN-LAST:event_btSalvarActionPerformed

